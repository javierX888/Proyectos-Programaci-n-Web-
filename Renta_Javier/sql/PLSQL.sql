RETURN NUMBER IS
    AID NUMBER;
BEGIN
    INSERT INTO ADDRESSES(STREET, POST_CODE, CITY_ID) 
    VALUES(STR, PC, CID) RETURNING ADDRESS_ID INTO AID;
    
    RETURN AID;
END;
/

-- Función IS_ROOM_AVAILABLE
CREATE OR REPLACE FUNCTION IS_ROOM_AVAILABLE(HID IN NUMBER, RN IN NUMBER, CID IN VARCHAR2, COD IN VARCHAR2, G IN NUMBER)
RETURN CHAR IS
    CNT NUMBER;
    CAPACITY NUMBER;
BEGIN
    SELECT MAX_CAPACITY INTO CAPACITY
    FROM ROOMS
    WHERE HOUSE_ID = HID
    AND ROOM_NO = RN;
    
    IF G <= CAPACITY THEN
        SELECT COUNT(ROOM_NO) INTO CNT
        FROM RENTS
        WHERE HOUSE_ID = HID
        AND ROOM_NO = RN
        AND ((TO_DATE(CID, 'DD-MON-YYYY') BETWEEN CHECKIN AND CHECKOUT)
        OR (TO_DATE(COD, 'DD-MON-YYYY') BETWEEN CHECKIN AND CHECKOUT)
        OR (CHECKIN BETWEEN TO_DATE(CID, 'DD-MON-YYYY') AND TO_DATE(COD, 'DD-MON-YYYY')));
    
        IF CNT = 0 THEN
            RETURN 'Y';
        ELSE
            RETURN 'N';
        END IF;
    END IF;
    RETURN 'N';
END;
/

-- Trigger UPDATE_DEPOSITS
CREATE OR REPLACE TRIGGER UPDATE_DEPOSITS
FOR INSERT
ON RENTS
COMPOUND TRIGGER
    UID NUMBER;
    HOST_BANK_ACC_NO VARCHAR2(35);
    
    BEFORE EACH ROW IS
    BEGIN
        SELECT USER_ID INTO UID
        FROM HOUSES WHERE HOUSE_ID = :NEW.HOUSE_ID;
        
        SELECT BANK_ACC_NO INTO HOST_BANK_ACC_NO
        FROM USERS WHERE USER_ID = UID;
        
        IF HOST_BANK_ACC_NO IS NULL THEN
            RAISE_APPLICATION_ERROR(-20001, 'Transaction cant be completed. Owner doesnt have bank account no');
        END IF;
    END BEFORE EACH ROW;

    AFTER EACH ROW IS
    BEGIN
        INSERT INTO DEPOSITS
        VALUES(UID, :NEW.TRANSACTION_ID, HOST_BANK_ACC_NO);
    END AFTER EACH ROW;
END;
/

-- Trigger MAKE_HOST
CREATE OR REPLACE TRIGGER MAKE_HOST
AFTER INSERT
ON HOUSES
FOR EACH ROW
DECLARE
    ISHOST NUMBER(1);
BEGIN
    DBMS_OUTPUT.PUT_LINE('Make host triggered ' || :NEW.USER_ID);
    UPDATE USERS
    SET IS_HOST = 1
    WHERE USER_ID = :NEW.USER_ID;
    DBMS_OUTPUT.PUT_LINE('Host ADDED');
END;
/

-- Trigger REMOVE_HOST
CREATE OR REPLACE TRIGGER REMOVE_HOST
AFTER DELETE
ON HOUSES
FOR EACH ROW
DECLARE
    NO_OF_HOUSES NUMBER;
    HID NUMBER := 0;
    PRAGMA AUTONOMOUS_TRANSACTION;
BEGIN
    DBMS_OUTPUT.PUT_LINE('Remove host triggered');
    SELECT COUNT(HOUSE_ID) INTO NO_OF_HOUSES
    FROM HOUSES
    WHERE USER_ID = :OLD.USER_ID;
    
    IF NO_OF_HOUSES = 1 THEN
        SELECT HOUSE_ID INTO HID
        FROM HOUSES
        WHERE USER_ID = :OLD.USER_ID;
    END IF;
    
    IF NO_OF_HOUSES = 1 AND HID = :OLD.HOUSE_ID THEN
        UPDATE USERS
        SET IS_HOST = 0
        WHERE USER_ID = :OLD.USER_ID;
        DBMS_OUTPUT.PUT_LINE('Host removed');
    END IF;
    COMMIT;
END;
/

